public class IfElse {
    public static void main(String args[]) {
        String weather = "sunny";
        if (weather == "raining") {
            System.out.println("I am staying home");
        } else {
            System.out.println("I am going out for a walk");
        }
    }
}

//If-else is conditional statements that execute according to the correct statement executed.
//If the if condition is true then the code block inside the if statement is executed else it
// executes the else statement block.